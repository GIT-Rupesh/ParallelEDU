a = [ 1, 9, 3, 3, 3, 3, 7, 8, 6, 4, 5, 3, 2 ]

print("Original list: ", a)
a.sort()
print("Sorted list: ", a)
a.reverse()
print("Reversed list: ", a)
print("Length of the list is: ", len(a))
print("Maximum value in the list is: ", max(a))
print("Minimum value in the list is: ", min(a)) 

print ("Count of 3 in the list is: ", a.count(3))   
